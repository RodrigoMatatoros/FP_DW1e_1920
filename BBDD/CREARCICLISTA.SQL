

DROP TABLE EQUIPO CASCADE ;
CREATE TABLE EQUIPO
       (NOMEQUIPO VARCHAR(25)  PRIMARY KEY,
	DIRECTOR VARCHAR(30));

DROP TABLE CICLISTA CASCADE ;
CREATE TABLE CICLISTA
       (DORSAL int  PRIMARY KEY,
	NOMBRE VARCHAR(30) NOT NULL,
	EDAD int,
	NOMEQUIPO VARCHAR(25)  REFERENCES EQUIPO(NOMEQUIPO)) ;

DROP TABLE ETAPA CASCADE ;
CREATE TABLE ETAPA
	(NUMETAPA int  PRIMARY KEY,
	 KMS int,
	 SALIDA VARCHAR(30),
	 LLEGADA VARCHAR(30),
	 DORSAL INT REFERENCES CICLISTA(DORSAL));

DROP TABLE MAILLOT CASCADE ;
CREATE TABLE MAILLOT
	(CODIGO VARCHAR(3)  PRIMARY KEY,
	 TIPO VARCHAR(30),
	 COLOR VARCHAR(20),
	 PREMIO INT);
	
DROP TABLE LLEVA CASCADE ;
CREATE TABLE LLEVA
	(DORSAL INT ,
	 NUMETAPA INT,
	 CODIGO VARCHAR(3)  REFERENCES MAILLOT(CODIGO));

DROP TABLE PUERTO CASCADE ;
CREATE TABLE PUERTO
	(NOMBREPUERTO VARCHAR(35)  PRIMARY KEY,
	 ALTURA INT,
	 CATEGORIA VARCHAR(1),
	 PENDIENTE INT,
	 NUMETAPA INT  REFERENCES ETAPA(NUMETAPA),
	 DORSAL INT  REFERENCES CICLISTA(DORSAL));
	 

